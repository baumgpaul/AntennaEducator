"""Unit tests for Phase 8 Step 6 — Projects service hardening.

Covers:
- Schema validation constraints (description, project_id bounds)
- IMAGE_KEY_PATTERN extension whitelist
- Folder cycle detection helper
"""

import pytest
from pydantic import ValidationError

from backend.projects.documentation_service import DocumentationService
from backend.projects.schemas import (
    ProjectBase,
    ProjectUpdate,
    SubmissionCreate,
)

# ══════════════════════════════════════════════════════════════════════════════
#  Schema Validation
# ══════════════════════════════════════════════════════════════════════════════


class TestProjectSchemaConstraints:
    """Verify max_length on description fields."""

    def test_description_within_limit(self):
        p = ProjectBase(name="Test", description="A" * 5000)
        assert len(p.description) == 5000

    def test_description_exceeds_limit(self):
        with pytest.raises(ValidationError, match="description"):
            ProjectBase(name="Test", description="A" * 5001)

    def test_description_none_allowed(self):
        p = ProjectBase(name="Test", description=None)
        assert p.description is None

    def test_update_description_within_limit(self):
        u = ProjectUpdate(description="B" * 5000)
        assert len(u.description) == 5000

    def test_update_description_exceeds_limit(self):
        with pytest.raises(ValidationError, match="description"):
            ProjectUpdate(description="B" * 5001)


class TestSubmissionSchemaConstraints:
    """Verify bounds on SubmissionCreate.project_id."""

    def test_project_id_valid(self):
        s = SubmissionCreate(project_id="abc-123")
        assert s.project_id == "abc-123"

    def test_project_id_empty_rejected(self):
        with pytest.raises(ValidationError):
            SubmissionCreate(project_id="")

    def test_project_id_too_long_rejected(self):
        with pytest.raises(ValidationError):
            SubmissionCreate(project_id="x" * 101)

    def test_project_id_at_max_length(self):
        s = SubmissionCreate(project_id="x" * 100)
        assert len(s.project_id) == 100


